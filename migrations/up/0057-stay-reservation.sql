	id serial NOT NULL,
	price DECIMAL(19, 4) NOT NULL,
	numadult int4 NOT NULL,
	numchildren int4 NOT NULL,
  numinfant int4 NOT NULL,
	currencyid int4 NOT NULL,
	note text NULL,
	purposetrip text NULL,
  expectedcheckintime text NOT NULL,
	reservationcode text NOT NULL,
	userinfo jsonb NULL,
